require 'pry'

class String

  def sentence?
    puts ("Hi, my name is Sophie.".sentence?)=(true)
  end

  def question?
     puts ("What's your name?".question?)=(true)
  end

  def exclamation?
     puts ("Hi, my name is Sophie!".exclamation?)=(true)
  end

  def count_sentences
      puts ("one. two. three?".count_sentences)=(3) 
  end
end